package BasicJava;

public class BitWiseOperator {
    public static void main(String[] args) {
        /*
        &--If both are 1 than Its 1
        |--If one Of 1 is 1 than Its 1
        << -- Values Are Double
        >> -- Value Are Half
        ~ -- Revert the bits

         */
        int x=10&7;
        System.out.println(x);
    }
}
